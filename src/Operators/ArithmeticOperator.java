package Operators;

public class ArithmeticOperator {
	
	public static void main(String[] args) {
        int a = 4;
        int var;
        var = a; // Assign
        System.out.println("var using =: " + var);
        var += a; // Add and assign
        System.out.println("var using +=: " + var);
        var *= a; // Multiply and assign
        System.out.println("var using *=: " + var);
    }
}
