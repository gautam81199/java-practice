package JavaClassesMethodsFieldsConstructors;

public class StaticConstantExamples {

	// Static final variable (constant)
	public static final double PI = 3.14159;
	public static final int MAXIMUM_ATTEMPTS = 5;
	public static final String GREETING_MESSAGE = "Hello, World!";

	public static void main(String[] args) {
		// Accessing the static final variables
		System.out.println("Value of PI: " + PI);
		System.out.println("Maximum Attempts: " + MAXIMUM_ATTEMPTS);
		System.out.println("Greeting Message: " + GREETING_MESSAGE);
	}

}
